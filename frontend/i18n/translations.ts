export function getTranslations(lang: SupportedLanguage): Translations {
  return translations[lang] || translations.en;
}

export function t(key: keyof Translations, lang: SupportedLanguage = 'en'): string {
  const trans = translations[lang] || translations.en;
  return trans[key] || translations.en[key] || key;
}

export function getLanguageConfig(code: SupportedLanguage): LanguageConfig | undefined {
  return SUPPORTED_LANGUAGES.find(l => l.code === code);
}

